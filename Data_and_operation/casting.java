
public class casting {

	public static void main(String[] args) {
		double a = 1.1;
		double b = 1;
		System.out.println(a);
		System.out.println(b);
		
		// double c =1.1;
		int d =(int) 1.1;
		System.out.println(d);


		String f = Integer.toString(1);
		System.out.println(f.getClass());
		System.out.println(f);
		
	}

}
